{{-- DEBATE --}}
<small class="text-muted">
    Debate is said to be one of the most exciting competitions with a huge array of participants. Hence, The 2022 NEO
    won’t miss out on bringing a valiant environment that will increase the critical and creative thinking of
    participants. This year’s National English Olympics will hold a British Parliamentary System Debate, Open Category.
    Challenge yourself for the prospect of winning a debate!
</small>

<hr class="my-4">

<h5 class="mb-3 text-primary">GENERAL RULES</h5>
<ol class="li-custom">
    <li>
        NEO 2022 will use the <b>British Parliamentary Debate</b> format,
        <b>Open-Category</b>. Each team will consist of 2 debaters.
    </li>
    <li>
        Every participant in this competition must be a <b>senior high school
            student</b> or <b>university student</b>.
    </li>
    <li>
        This competition has 2 sets of rounds:
        <ol style="list-style-type: lower-alpha;">
            <li class="fw-semibold">Preliminary Rounds (4 rounds)</li>
            <p class="mb-2">
                All teams will compete in four rounds; to proceed to the elimination
                rounds, the team must get the highest score possible to get a spot
                in the breaking teams. The breaking teams consist of 16 teams with
                the victory points accumulated from rankings of each round.
            </p>
            <li class="fw-semibold">Elimination Rounds</li>
            <p class="mb-2">
                All teams will compete in four rounds; to proceed to the elimination
                rounds, the team must get the highest score possible to get a spot
                in the breaking teams. The breaking teams consist of 16 teams with
                the victory points accumulated from rankings of each round.
            </p>
            <ol style="list-style-type: lower-roman;">
                <li class="fw-semibold">Quarter-final Round</li>
                <p class="mb-2">
                    16 best breaking teams will compete in four rooms, only 8 teams
                    will move forward to the next round.
                </p>
                <li class="fw-semibold">Semi-final Round</li>
                <p class="mb-2">
                    8 teams from the quarter-final round will compete in two rooms,
                    only 4 teams will move forward to the next round.
                </p>
                <li class="fw-semibold">Grand-final Round</li>
                <p class="mb-2">
                    The final round in which 4 teams will compete to determine the
                    winner of the National English Olympics 2022 Debate Competition.
                </p>
            </ol>
        </ol>
    </li>
    <li>
        The <b>preliminary rounds</b> of the competition is held <b>online</b> via
        Zoom App whilst the <b>quarter-final, semi-final, and grand final</b> are
        held <b> onsite</b> at BINUS University Alam Sutera.
    </li>
    <li>
        Participants of a debate team who have registered online can be
        <b>replaced</b> with a new participant <b>maximum 2 days</b> before the
        registration period ends.
        Replacement after the given time will result in <b>disqualification</b>.
    </li>
    <li>
        The Zoom link for the competition will be sent to the participants via email
        and WhatsApp group <b>one day</b> before the D-Day of the competition round.
    </li>
    <li>
        Every participant <b>must</b> re-register themselves by attending the
        Technical Meeting on <b>Monday, 7th November, 2022</b>.
        Participants who failed to attend this will be <b> disqualified</b>.
        There will be an exception for those who are not able to join due to
        personal emergencies
        in which a permission of absence must be sent to the committee <b>maximum 1
            day before</b> the Technical Meeting.
    </li>
    <li>Participants <b>must</b> use English during the competition.
        The speech <b>must not</b> conduct discrimination against SARA nor contain
        graphic sexual content.
        Participants <b>must not</b> include any form of profanity.
    </li>
    <li>Participants are highly recommended to have a <b>backup internet
            connection</b>, such as mobile data or other wifi connections.
        If the participants experience unstable connections in the competition,
        it is <b>not the responsibility</b> of the NEO 2022 committee.
    </li>
    <li>The debate will be <b>recorded</b> to avoid any misjudgement from a bad
        connection.
    </li>
    <li>Participants <b>must</b> wear semi-formal or formal clothes.</li>
    <li>All adjudicators’ decisions and results are <b>final</b>.</li>
</ol>

<hr class="my-4">

<h5 class="mb-3 text-primary">TECHNICAL RULES</h5>
<ol class="li-custom">
    <li>
        All participants <b>must</b> join the waiting room at least 30 minutes
        before the event starts.
    </li>
    <li>
        Draw Release will be published through the Tabbycat App and the link to it
        will be sent to the general WhatsApp group. Participants are able to access
        the Tabbycat app.
    </li>
    <li>
        After all of the participants have been informed about their draws and
        positions, they <b>must rename</b> their Zoom display name with the format:
        <br>
        <b>Example: R1_A5_CO_James</b>
    </li>
    <li>A team is allowed to use one device for the two members to perform; just
        change the Zoom display name with the format of: Round_Room_Position_Name &
        Name
        <br>
        <b>Example: R3_A2_CO_James & Dina</b>
    </li>
    <li>
        Debaters will be given <b>15 minutes for case building</b> after the motion
        is released.
    </li>
    <li>
        When the motion is released, breakout rooms (the debate room) will be
        opened. Then, participants will be assigned to their predetermined debate
        room.
    </li>
    <li>
        If debater(s) are <b>not present</b> when the round starts, all debaters in
        the room will wait for the missing debater(s) for <b>5 minutes</b> while the
        committee contacts them. If the missing debater(s) are <b>still not
            present</b> after 5 minutes, the committee will send a <b>swing team</b>
        to the room. The missing debater(s) will be <b>disqualified</b>.
    </li>
    <li>
        If a debater suddenly <b>get disconnected</b> when debating:
        <ol style="list-style-type: lower-alpha;">
            <li>
                If a debater is unable to reconnect to the Zoom within 5 minutes to
                do the speech, your teammate must call you with a loud speaker thus
                enabling you to continue the speech.
            </li>
            <li>
                If option (a.) does not work, the <b>Iron Policy applies</b>. Your
                teammate can replace your position to do the speech. But you will
                get the lowest score (50) on the tab for that round.
            </li>
        </ol>
    </li>
    <li>
        Please state your <b>POI preference</b> before starting your speech. If not,
        then we will automatically assume that the POI will be delivered through the
        chat box by writing “POI”.
    </li>
    <li>
        After the debate round is finished, participants will go back to the main
        room and wait for the judges to deliberate their scores. Debaters will then
        return to each chamber to listen to the judges’ OA.
    </li>
    <li>
        After the OA/Verbal is delivered to debaters, all teams are encouraged to
        give adjudicators feedback scores based on the OA/Verbal given by the
        participants’ chair. The adjudicators’ feedback can be given via the tab
        page.
    </li>
</ol>

{{-- NEWSCASTING --}}
<small class="text-muted">
    Newscasting is a competition that’s not only exciting but also thrilling! It is a competition where it challenges
    your public speaking and body language skill to capture all of the audience’s attention to the news you report. It
    serves you the experience to simulate reporting news, live reports, and even conducting a talk show with a special
    guest and cases! Come and challenge yourself with our newscasting competition, which will give you the space to
    enhance your skills and expand your network!
</small>

<hr class="my-4">

<h5 class="mb-3 text-primary">RULES AND REGULATION</h5>
<ol>
    <li>
        Every participant in this competition must be a <b>senior high school</b> or
        <b>university student</b>.
    </li>
    <li>
        The scoring aspect for the competition will be Performance, Material, and
        Matter (the quality of the wording chosen, the grammar, and pronunciation).
    </li>
    <li>
        The script must be original work. Plagiarism found by or notified to the
        judges may lead to disqualification.
    </li>
    <li>
        Participants must use English during the competition. The script shall not
        contain discrimination against SARA nor contain graphic sexual content.
        Participants must not include any form of profanity.
    </li>
    <li>
        Bringing electronic devices such as laptops, tabs, and so on is optional.
        However, it is suggested to bring pencils/pens or any stationery for cue
        purposes.
    </li>
    <li>
        Participants are allowed to research their topic using their phones or other
        electronic devices but are <b>not allowed to copy-paste</b>. By joining this
        competition, all participants have agreed to be watched and supervised by
        the LOs.
    </li>
    <li>
        Participants are encouraged to appear formal, clean, and neat, including but
        not limited to hair neatness.
    </li>
    <li>
        Participants are allowed to use the appropriate amount of makeup.
    </li>
</ol>

<hr class="my-4">

<h5 class="mb-3 text-primary">ROUNDS GUIDELINE</h5>

<h6 class="fw-semibold">Preliminary Round: News Reading (Online, Via Zoom)</h6>
<ol class="li-custom">
    <li>
        Participants will be given articles regarding the topic given by the
        committees on D-Day.
    </li>
    <li>
        Proposed topic: How Technology is Shaping the Future.
    </li>
    <li>
        Each participant will draw to determine which article they will read.
    </li>
    <li>
        They will have to perform the news-reading for a maximum of 5 minutes. The
        committees will remind the participants through the Zoom chat box every
        4 minutes as a sign to wrap the reading up and 5 minutes as a sign to end
        the reading.
    </li>
    <li>
        Participants will get 10 minutes to become familiar with the topic given.
    </li>
    <li>
        15 participants will carry on to the Semi-Final round.
    </li>
</ol>

<hr class="border-0">

<h6 class="fw-semibold">Semi-Final Round: Live Report (Offline)</h6>
<ol class="li-custom">
    <li>
        Participants are obliged to create their report from the given case.
    </li>
    <li>
        Some of the proposed cases:
        <ul style="list-style-type: square;">
            <li>Pandemic is Altering Educational Technology</li>
            <li>Is Indonesia Capable of Creating a Robust Fintech Environment?</li>
            <li>Women in E-Sport</li>
        </ul>
    </li>
    <li>
        They will have to perform for a minimum of 4 minutes and a maximum of 6
        minutes. The committee will give signals as a reminder every 4 minutes,
        5
        minutes as a sign to wrap the report up, and 6 minutes as a sign to end
        the
        talk show.
    </li>
    <li>
        If the participant hasn’t finished the talk show, the committee will
        stop
        the talk show.
    </li>
    <li>
        Participants will get 15 minutes to prepare the report script and 10
        minutes
        to practice.
    </li>
    <li>
        Participants are not allowed to bring scripts. However, making a cue
        card is
        permitted.
    </li>
</ol>

<hr class="border-0">

<h6 class="fw-semibold">Final Round: Talk Show (Offline)</h6>
<ol class="li-custom">
    <li>
        Participants will be given a CV and resume of the guest 20 minutes prior to
        performance time.
    </li>
    <li>
        The guest will be provided by the committee based on the given case. This
        will be as if we’re reporting a live situation.
    </li>
    <li>
        Participants are not allowed to bring scripts. However, making a cue card is
        permitted.
    </li>
    <li>
        They will have to perform for a minimum of 5 minutes and a maximum of 10
        minutes. There will be a reminder for every participant through a cue card
        by the LOs every 5 minutes, 9 minutes as a sign to wrap the news up, and 10
        minutes to mark the end of the talk show.
    </li>
    <li>
        If the participant hasn’t finished the talk show, the committee will stop
        the talk show.
    </li>
</ol>

